n1 = int(input("Digite um número: "))

n2 = int(input("Digite outro número: "))

soma = n1 + n2
subtrair = n1 - n2
multiplicar = n1 * n2
dividir = n1 / n2
resto = n1 % n2
print(f"O resultado da soma é {soma} o tipo da variável é {type(soma)}")
print(f"O resultado da subtração é {subtrair} o tipo da variável é {type(subtrair)}")
print(f"O resultado da multiplicação é {multiplicar} o tipo da variável é {type(multiplicar)}")
print(f"O resultado da divisão é {dividir} o tipo da variável é {type(dividir)}")
print(f"O resultado do resto é {resto} o tipo da variável é {type(resto)}")